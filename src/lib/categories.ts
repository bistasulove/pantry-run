// Precompute (keyword, category) tuples sorted by keyword length DESC so that
// a multi-word keyword like "sweet potato" wins over "potato", and
// "peanut butter" wins over "peanut".
const SORTED_TUPLES: Array<{ keyword: string; category: Category }> = (() => {
  const tuples: Array<{ keyword: string; category: Category }> = []
  for (const [category, keywords] of Object.entries(CATEGORY_KEYWORDS) as Array<
    [Exclude<Category, 'Other'>, string[]]
  >) {
    for (const keyword of keywords) {
      tuples.push({ keyword: keyword.toLowerCase(), category })
    }
  }
  return tuples.sort((a, b) => b.keyword.length - a.keyword.length)
})()

/**
 * Tier 1 — fast offline keyword match. Returns null on miss so the caller
 * can decide whether to fall through to the LLM (and flag the row as
 * `category_pending`).
 */
export function detectCategoryKeyword(itemName: string): Category | null {
  const haystack = ` ${itemName.toLowerCase().trim()} `
  for (const { keyword, category } of SORTED_TUPLES) {
    if (haystack.includes(` ${keyword} `)) {
      return category
    }
  }
  return null
}

export interface RemoteCategoryResult {
  category: Category
  source: 'household' | 'global_cache' | 'llm' | 'rate_limited' | 'error'
}

/**
 * Tier 2 — calls the categorize_item Supabase Edge Function. Online-only.
 *
 * Returns a typed source so the caller can decide how to treat the result
 * (e.g. don't clear `category_pending` on 'rate_limited' or 'error' — the
 * reconnect sweep will retry next session).
 *
 * Throws only on transport / auth failure; non-OK function responses come
 * back as { category: 'Other', source: 'error' } per the Edge Function
 * contract.
 */
export async function categorizeRemote(
  name: string,
  householdId: string,
): Promise<RemoteCategoryResult> {
  const supabase = createClient()
  const { data, error } = await supabase.functions.invoke<RemoteCategoryResult>('categorize_item', {
    body: { name, household_id: householdId },
  })

  if (error || !data) {
    return { category: FALLBACK_CATEGORY, source: 'error' }
  }

  if (typeof data.category !== 'string' || !isCategory(data.category)) {
    return { category: FALLBACK_CATEGORY, source: 'error' }
  }

  return { category: data.category, source: data.source ?? 'error' }
}
