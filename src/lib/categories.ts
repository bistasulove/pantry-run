// Pantry Run — category detection (M15 two-tier)
//
// Tier 1: detectCategoryKeyword (sync, offline-safe). Returns null on miss so
// the caller can decide whether to fall through to the LLM.
// Tier 2: categorizeRemote (async, online-only). Calls the categorize_item
// Supabase Edge Function which checks household + global cache before
// hitting Gemini, and writes back to the global cache on a fresh LLM result.
//
// CATEGORY_ORDER is the single source of truth for both the picker UI and
// the section ordering on /list. The 4 new entries (Snacks, Condiments &
// Sauces, Baby, Pet) sit before 'Other' so unknown items still sort last.
//
// Keyword matching is case-insensitive, prefers the longest matching keyword,
// and only matches whole words so "creamy" doesn't sort into Dairy. Multi-
// word keywords like "peanut butter" win over single-word "peanut" thanks to
// the longest-first sort below — exploit this for disambiguation rather than
// special-casing in code.

import { createClient } from '@/lib/supabase/client'

export const CATEGORY_ORDER = [
  'Produce',
  'Dairy',
  'Meat',
  'Bakery',
  'Pantry',
  'Frozen',
  'Beverages',
  'Household',
  'Personal Care',
  'Snacks',
  'Condiments & Sauces',
  'Baby',
  'Pet',
  'Other',
] as const

export type Category = (typeof CATEGORY_ORDER)[number]

export const FALLBACK_CATEGORY: Category = 'Other'

// All categories the categorize_item function may return. Mirror of the
// Edge Function's ALL_CATEGORIES, kept in sync via the CHECK constraints
// on the DB tables.
const ALLOWED_CATEGORIES: ReadonlySet<Category> = new Set(CATEGORY_ORDER)

function isCategory(value: string): value is Category {
  return ALLOWED_CATEGORIES.has(value as Category)
}

